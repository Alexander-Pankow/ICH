# остальными двумя, используя операторы `больше`, `меньше` и `равны`.
# Пример вывода:
# Введите первое число: 7
# Введите второе число: 5
# Введите третье число: 7
# Первое больше второго: True
# Первое меньше третьего: False
# Первое равно третьему: True

# Получаем три числа от пользователя
first = int(input("Введите первое число: "))
second = int(input("Введите второе число: "))
third = int(input("Введите третье число: "))

# Сравниваем числа
is_greater = first > second
is_less = first < third
is_equal = first == third

# Выводим результаты сравнения
print("Первое больше второго:", is_greater)
print("Первое меньше третьего:", is_less)
print("Первое равно третьему:", is_equal)